ox_quiz_format = """
    ### 형식(필수)
    - 문제 유형: OX (True/False)
    - 문제 1개는 아래 필드를 반드시 포함
      - number: 문제 번호(정수)
      - title: 참/거짓을 판단할 수 있는 단일 진술문(문장)
      - selections: 선택지 2개(각 항목은 {content, correct})
      - explanation: 해설(문장형 텍스트)
    - selections 제약
      - 정확히 2개만 존재
      - content는 아래 두 값만 사용(표기 통일):
        - \"O\" (참)
        - \"X\" (거짓)
      - correct=True는 정확히 1개만 존재
"""

ox_guidelines = """

    ### 목적(핵심)
    - **난이도**: DOK 2~3(개념 적용·분석) 수준의 판단형 OX 문항을 만든다.
    - **평가 목표**: 단순 암기(DOK 1)가 아니라, 조건/맥락/인과를 고려해 \"왜 참/거짓인지\"를 검증하도록 한다.

    ---

    ### 문항 작성 규칙(필수)
    - title은 참/거짓이 **명확히 판별 가능한 단일 진술문**이어야 한다.
    - 단순한 상식/너무 명백한 사실문은 피한다.
    - 가능한 한 **조건/인과/비교/예외**가 포함된 문장으로 만들어 미묘함을 확보한다.
      - 예: \"A인 경우에는 B가 C를 보장한다\", \"D를 E보다 우선하면 F가 발생한다\"
    - 내부적으로 [전제 → 추론 → 결론]의 Reasoning Chain을 생각하되, 지문에 추론 과정을 과도하게 드러내지 않는다.
    - 강의노트 직접 참조 표현(“본문/강의노트에 따르면…”)은 사용하지 않는다.

    ---

    ### 함정/균형(권장)
    - 가능하면 생성되는 문항들에서 O(참)와 X(거짓)가 **대략 절반씩** 나오도록 한다.
    - X(거짓)는 흔한 오개념/과잉 일반화/맥락 무시를 반영하되, 억지 오류는 피한다.

    ---

    ### 해설 작성 규칙(간결)
    - 최대 3~4문장.
    - 왜 참/거짓인지 핵심 근거(조건/인과/예외)를 1~2문장으로 설명한다.
    - 학습자가 자주 착각하는 포인트(맥락/조건 누락 등)를 1문장으로 짚는다.

    ---

    ### 최종 체크리스트
    - [ ] selections가 정확히 2개이며 content가 \"O\"와 \"X\"만 포함하는가?
    - [ ] correct=True가 정확히 1개인가?
    - [ ] title이 모호하지 않고 참/거짓이 명확히 판별되는가?
    - [ ] 해설이 근거를 포함하며 4문장 이내인가?

    """
